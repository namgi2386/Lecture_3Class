bit = "0000000111100000011000000111100110000110000111100111100111111001100111"
"0 120 12 7 76 24 60 121 124 103"

# 이진수를 7칸씩 쪼개서 십진수로 만들기
N = len(bit)

for i in range(0,N,7):
    # i번째 비트부터 7칸 가져온 다음에 십진수로 만들기
    dec = 0  # 십진수로 바꾼 결과
    # 2^0 * bit[i+6]
    # 2^1 * bit[i+5]
    # ..
    # 2^6 * bit[i+0]
    for j in range(7):
        dec += (2**j) * int(bit[i+6-j])
    print(dec, end=" ")
